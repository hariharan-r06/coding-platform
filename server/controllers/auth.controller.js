const jwt = require('jsonwebtoken');
const bcrypt = require('bcryptjs');
const supabase = require('../config/supabase');

const register = async (req, res) => {
    try {
        const { email, password, full_name } = req.body;

        if (!email || !password || !full_name) {
            return res.status(400).json({ error: 'Please provide all required fields' });
        }

        // 1. Create user in Supabase Auth
        // Using signUp as it works with anon/service keys without full admin role on the key itself
        const { data: authData, error: authError } = await supabase.auth.signUp({
            email,
            password,
            options: {
                data: { full_name }
            }
        });

        if (authError) {
            return res.status(400).json({ error: authError.message });
        }

        if (!authData.user) {
            // If manual confirmation is on, user might be null or session null.
            // But usually user is returned.
            return res.status(400).json({ error: 'Registration failed. Please check your email for confirmation or try again.' });
        }

        // 2. Create user profile in public.users table
        // We use the same ID generated by Supabase Auth
        const { data: newUser, error: profileError } = await supabase
            .from('users')
            .insert([
                {
                    id: authData.user.id,
                    email,
                    full_name,
                    role: 'user',
                    password_hash: 'managed_by_supabase_auth' // Placeholder to satisfy NOT NULL constraint
                }
            ])
            .select('id, email, full_name, role')
            .single();

        if (profileError) {
            // Rollback auth user creation if profile creation fails?
            // Ideally yes, but for now just logging it.
            console.error('Profile creation failed:', profileError);
            // Delete the auth user to keep consistency
            await supabase.auth.admin.deleteUser(authData.user.id);
            throw profileError;
        }

        // 3. Create Local JWT (maintain existing auth flow)
        const token = jwt.sign(
            { id: newUser.id, role: newUser.role },
            process.env.JWT_SECRET,
            { expiresIn: process.env.JWT_EXPIRES_IN }
        );

        res.status(201).json({
            user: newUser,
            token
        });
    } catch (error) {
        console.error('Registration error:', error.message);
        res.status(500).json({ error: 'Server error during registration' });
    }
};

const login = async (req, res) => {
    try {
        const { email, password } = req.body;

        if (!email || !password) {
            return res.status(400).json({ error: 'Please provide email and password' });
        }

        // 1. Authenticate with Supabase Auth
        const { data: authData, error: authError } = await supabase.auth.signInWithPassword({
            email,
            password
        });

        if (authError) {
            return res.status(401).json({ error: 'Invalid credentials' });
        }

        // 2. Fetch user profile from public.users table
        const { data: user, error: profileError } = await supabase
            .from('users')
            .select('*')
            .eq('id', authData.user.id) // Match by ID since email is verified
            .single();

        if (profileError || !user) {
            // Handle edge case where user is in Auth but not in public.users (legacy data or partial failure)
            return res.status(401).json({ error: 'User profile not found locally' });
        }

        // 3. Create Local JWT
        const token = jwt.sign(
            { id: user.id, role: user.role },
            process.env.JWT_SECRET,
            { expiresIn: process.env.JWT_EXPIRES_IN }
        );

        // Remove password hash from response
        const { password_hash, ...userWithoutPassword } = user;

        res.json({
            user: userWithoutPassword,
            token
        });
    } catch (error) {
        console.error('Login error:', error.message);
        res.status(500).json({ error: 'Server error during login' });
    }
};

